# Day 16: Reindeer Maze

from heapq import *

def solve(data):
    k = 0
    A = {i + 1j * j: v for i, r in enumerate(data.split()) for j, v in enumerate(r)}
    S, E = (next(pos for pos, cell in A.items() if cell == ch) for ch in 'SE')
    pq = [(0, k, S, 1, {S})]
    ds = {(S, 1): (0, {S})}
    while pq:
        cur_dist, _, cur_pos, cur_dir, cur_sit = heappop(pq)
        for new_dir in range(4):
            new_pos = cur_pos + 1j ** new_dir
            new_dist = cur_dist + [1001, 1][cur_dir == new_dir]
            known_dist, known_sit = ds.get((new_pos, new_dir), (new_dist + 1, set()))
            if new_dist <= known_dist and A[new_pos] != '#':
                new_sit = cur_sit | {new_pos}
                if new_dist == known_dist:
                    new_sit |= known_sit
                ds[new_pos, new_dir] = new_dist, new_sit
                k += 1
                heappush(pq, (new_dist, k, new_pos, new_dir, new_sit))
    part1 = min(ds[x][0] for x in ds if x[0] == E)
    part2 = {S}
    for (pos, _), (dist, sit) in ds.items():
        if pos == E and dist == part1:
            part2 |= sit
    yield part1, len(part2)


