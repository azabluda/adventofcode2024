def solve(data: str):
    X = defaultdict(list)
    for s in data.split('\n\n'):
        X[s[0][0] == '#'] += [col.count('#') for col in zip(*s.split())],
    print(sum(all(s <= 7 for s in map(add, k, l)) for k, l in product(*X.values())))


